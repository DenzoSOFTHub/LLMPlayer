__device__ __constant__ unsigned int KMASK_IQ2XS[8] = { 1, 2, 4, 8, 16, 32, 64, 128 };

extern "C" __global__ void matmul_iq2_s(
    const unsigned char* __restrict__ weights,
    const float* __restrict__ input,
    float* __restrict__ output,
    const int rows,
    const int cols,
    const int addToOutput)
{
    int warpId = threadIdx.x / 32;
    int lane = threadIdx.x & 31;
    int rowsPerBlock = blockDim.x / 32;
    int row = blockIdx.x * rowsPerBlock + warpId;
    if (row >= rows) return;

    int numSuperBlocks = cols / 256;
    int rowStride = numSuperBlocks * 82;
    float sum = 0.0f;

    for (int sb = lane; sb < numSuperBlocks; sb += 32) {
        int bo = row * rowStride + sb * 82;
        float d = half2float(*(unsigned short*)(weights + bo));

        int inputBase = sb * 256;

        // 8 groups of 32 weights
        #pragma unroll
        for (int ib32 = 0; ib32 < 8; ib32++) {
            int scaleByte = __ldg(weights + bo + 74 + ib32); // OFF_SCALES
            float db0 = d * (0.5f + (float)(scaleByte & 0xF)) * 0.25f;
            float db1 = d * (0.5f + (float)((scaleByte >> 4) & 0xF)) * 0.25f;

            int qhVal = __ldg(weights + bo + 66 + ib32); // OFF_QH

            // 4 sub-groups of 8 weights
            #pragma unroll
            for (int l = 0; l < 4; l++) {
                float dl = (l < 2) ? db0 : db1;

                // 10-bit grid index: 8 low bits from qs + 2 high bits from qh
                int qsVal = __ldg(weights + bo + 2 + ib32 * 4 + l); // OFF_QS
                int gridIdx = qsVal | ((qhVal << (8 - 2 * l)) & 0x300);

                unsigned long long grid = IQ2S_GRID[gridIdx];

                // Sign byte
                int signByte = __ldg(weights + bo + 34 + ib32 * 4 + l); // OFF_SIGNS

                int weightIdx = inputBase + ib32 * 32 + l * 8;

                // 8 elements from grid
                #pragma unroll
                for (int j = 0; j < 8; j++) {
                    int gv = (int)((grid >> (8 * j)) & 0xFF);
                    float sign = (signByte & (1 << j)) ? -1.0f : 1.0f;
                    sum += dl * (float)gv * sign * input[weightIdx + j];
                }
            }
        }
    }

    // Warp shuffle reduction
    for (int offset = 16; offset > 0; offset >>= 1)
        sum += __shfl_down_sync(0xFFFFFFFF, sum, offset);

    if (lane == 0) {
        if (addToOutput) output[row] += sum;
        else output[row] = sum;
    }
}

/**
 * IQ2_S dequantize + matrix-vector multiply kernel.
 * 256 weights per super-block, 82 bytes per block.
 * Layout: [d:fp16 (2B)][qs:32B grid index low][signs:32B][qh:8B grid index high][scales:8B]
 *
 * 8 groups of 32 weights. Each group has:
 *   - 1 scale byte (low nibble for first 16, high nibble for last 16)
 *   - 1 qh byte (2 high bits per sub-group of 8)
 *   - 4 qs bytes (grid index low 8 bits per sub-group of 8)
 *   - 4 sign bytes (1 bit per weight)
 *
 * Grid index (10 bits) -> IQ2S_GRID (1024 uint64 entries), each encoding 8 unsigned byte values.
 * Scale formula: d * (0.5 + scale_nibble) * 0.25
 *
 * Each warp (32 threads) computes one output row, striping across super-blocks.
 */
__device__ __forceinline__ float half2float(unsigned short h) {
    unsigned int sign = (h >> 15) & 1;
    unsigned int exp = (h >> 10) & 0x1F;
    unsigned int mantissa = h & 0x3FF;
    if (exp == 0) {
        if (mantissa == 0) return sign ? -0.0f : 0.0f;
        while (!(mantissa & 0x400)) { mantissa <<= 1; exp--; }
        exp++; mantissa &= 0x3FF;
    } else if (exp == 31) {
        unsigned int f = (sign << 31) | 0x7F800000 | (mantissa << 13);
        return *(float*)&f;
    }
    unsigned int f = (sign << 31) | ((exp + 112) << 23) | (mantissa << 13);
    return *(float*)&f;
}

